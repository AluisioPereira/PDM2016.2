package br.edu.ifpb.pdm;

public class Mensagem {
	private int id;
	private String texto;

	public Mensagem() {
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTexto() {
		return texto;
	}

	public void setTexto(String texto) {
		this.texto = texto;
	}

}
